package chess;

import TabuleiroGame.Posicao;

public class ChessPosiçao{
	
	private char coluna;
	private int linha;
	
	public ChessPosiçao(char coluna, int linha) {
		if(coluna < 'a' || coluna > 'h' || linha < 1 || linha > 8) {
			throw new ChessException("Erro instanciando ChessPosicao.Valores validos são de a1 até h8");
		}
		
		this.coluna = coluna;
		this.linha = linha;
	}

	public char getColuna() {
		return coluna;
	}

	public int getLinha() {
		return linha;
	}

	protected Posicao toPosiçao() {
		return new Posicao(8 - linha, coluna - 'a');
	}
	
	protected static ChessPosiçao fromPosiçao(Posicao posicao) {
		return new ChessPosiçao((char)('a' + posicao.getColuna()), 8 - posicao.getLinha());
	}
	
	@Override
	public String toString() {
		return "" + coluna + linha;
	}
	
	
}
